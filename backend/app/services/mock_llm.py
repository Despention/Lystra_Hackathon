class MockLLMClient:
    async def complete(self, system_prompt: str, user_prompt: str) -> str:
        agent_name = self._detect_agent(system_prompt)
        await asyncio.sleep(random.uniform(0.5, 1.5))

        if agent_name == "summary":
            return MOCK_SUMMARY
        if agent_name == "correction":
            return json.dumps(MOCK_CORRECTIONS, ensure_ascii=False)
        if agent_name == "improved":
            return MOCK_IMPROVED_TEXT
        if agent_name == "chat":
            reply = random.choice(MOCK_CHAT_REPLIES)
            # substitute score placeholder if present in user_prompt
            score_hint = "?"
            for word in user_prompt.split():
                if word.isdigit() and 0 <= int(word) <= 100:
                    score_hint = word
                    break
            return reply.replace("{score}", score_hint)
        if agent_name == "structure":
            return MOCK_TZ_STRUCTURE
        if agent_name == "example":
            return MOCK_TZ_EXAMPLE

        return json.dumps(MOCK_RESPONSES.get(agent_name, MOCK_RESPONSES["structural"]), ensure_ascii=False)

    async def stream(self, system_prompt: str, user_prompt: str) -> AsyncGenerator[str, None]:
        agent_name = self._detect_agent(system_prompt)
        response = json.dumps(MOCK_RESPONSES.get(agent_name, MOCK_RESPONSES["structural"]), ensure_ascii=False, indent=2)
        for char in response:
            yield char
            await asyncio.sleep(random.uniform(0.005, 0.02))

    def _detect_agent(self, system_prompt: str) -> str:
        prompt_lower = system_prompt.lower()
        if "summary" in prompt_lower or "резюме" in prompt_lower:
            return "summary"
        elif "улучшен" in prompt_lower or "редактор техническ" in prompt_lower:
            return "improved"
        elif "ассистент" in prompt_lower or "ai-ассистент" in prompt_lower:
            return "chat"
        elif "рекомендуемую структуру" in prompt_lower or "generate_structure" in prompt_lower:
            return "structure"
